#include <stdio.h>

static const char *GetMessage(void) {
  return "USAGE:\n"
         "  pvault --add SERVICE\n"
         "  pvault --find SERVICE\n"
         "  pvault --remove SERVICE\n"
         "  pvault --list [all|N]\n"
         "  pvault --dump\n"
         "  pvault -h | --help\n"
         "\n"
         "DESCRIPTION:\n"
         "  Local CLI password manager written in C. Encrypts credentials "
         "using libsodium.\n"
         "  Uses a background daemon to keep the vault unlocked for the "
         "duration of a user session.\n"
         "  Client communicates with the daemon over a UNIX domain socket.\n"
         "\n"
         "COMMANDS:\n"
         "  --add SERVICE\n"
         "      Add an entry for SERVICE.\n"
         "      Prompts for username (optional), password (Enter to "
         "auto-generate), link (optional).\n"
         "      If SERVICE already exists, it is replaced.\n"
         "\n"
         "  --find SERVICE\n"
         "      Print service, username, password, link for SERVICE.\n"
         "\n"
         "  --remove SERVICE\n"
         "      Delete the entry for SERVICE.\n"
         "\n"
         "  --list [all|N]\n"
         "      No argument: list up to 5 entries\n"
         "      all: list every entry\n"
         "      N: list the first N entries\n"
         "\n"
         "  --dump\n"
         "      Create a plaintext dump of all entries at:\n"
         "      ~/Downloads/pvault_dump_XXXXXX.txt\n"
         "\n"
         "OPTIONS:\n"
         "  -h, --help\n"
         "      Show this help message.\n";
}
